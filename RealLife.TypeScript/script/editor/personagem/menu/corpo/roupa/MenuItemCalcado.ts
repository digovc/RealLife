module RealLife
{
    // #region Importações
    // #endregion Importações

    // #region Enumerados
    // #endregion Enumerados

    export class MenuItemCalcado extends MenuItem
    {
        // #region Constantes
        // #endregion Constantes

        // #region Atributos
        // #endregion Atributos

        // #region Construtores

        constructor()
        {
            super(null, "Calçado");
        }

        // #endregion Construtores

        // #region Métodos

        protected inicializarArrObjMenuItem(arrObjMenuItem: Array<MenuItemBase>): void
        {
            super.inicializarArrObjMenuItem(arrObjMenuItem);

            arrObjMenuItem.push(new MenuItem(this, "Chinelo"));
            arrObjMenuItem.push(new MenuItem(this, "Tênis"));
            arrObjMenuItem.push(new MenuItem(this, "Sapato"));
        }

        // #endregion Métodos

        // #region Eventos
        // #endregion Eventos
    }
}